@app.route('/api/disk/drives')
def get_all_drives():
    """Get all available drives on the system"""
    try:
        import psutil
        import platform
        
        drives = []
        
        if platform.system() == 'Windows':
            # Get all disk partitions on Windows
            for partition in psutil.disk_partitions():
                try:
                    usage = psutil.disk_usage(partition.mountpoint)
                    drives.append({
                        'path': partition.mountpoint,
                        'name': partition.device,
                        'total_size': usage.total,
                        'used_size': usage.used,
                        'free_size': usage.free,
                        'usage_percent': usage.percent,
                        'total_size_formatted': disk_analyzer.format_size(usage.total),
                        'used_size_formatted': disk_analyzer.format_size(usage.used)
                    })
                except (PermissionError, OSError):
                    continue
        else:
            # Unix/Linux/Mac
            for partition in psutil.disk_partitions():
                if partition.fstype:
                    try:
                        usage = psutil.disk_usage(partition.mountpoint)
                        drives.append({
                            'path': partition.mountpoint,
                            'name': partition.mountpoint,
                            'total_size': usage.total,
                            'used_size': usage.used,
                            'free_size': usage.free,
                            'usage_percent': usage.percent,
                            'total_size_formatted': disk_analyzer.format_size(usage.total),
                            'used_size_formatted': disk_analyzer.format_size(usage.used)
                        })
                    except (PermissionError, OSError):
                        continue
        
        return jsonify({'drives': drives})
    except Exception as e:
        print(f"Get drives error: {e}")
        return jsonify({'error': str(e)}), 500

@app.route('/api/disk/treemap')
def get_treemap_data():
    """Get directory tree data for treemap visualization - full recursive scan for accuracy"""
    try:
        import os as _os
        path = request.args.get('path', None)

        if not path:
            return jsonify({'error': 'Path parameter required'}), 400

        path_normalized = _os.path.normpath(path)

        if not _os.path.isdir(path_normalized):
            return jsonify({'directories': [], 'path': path})

        def get_dir_size(dir_path):
            """Fully recursive directory size calculation"""
            total_size = 0
            file_count = 0
            stack = [dir_path]
            while stack:
                current = stack.pop()
                try:
                    for entry in _os.scandir(current):
                        try:
                            if entry.is_file(follow_symlinks=False):
                                total_size += entry.stat(follow_symlinks=False).st_size
                                file_count += 1
                            elif entry.is_dir(follow_symlinks=False):
                                stack.append(entry.path)
                        except (PermissionError, OSError):
                            continue
                except (PermissionError, OSError):
                    continue
            return total_size, file_count

        children = []

        try:
            entries = list(_os.scandir(path_normalized))
        except PermissionError:
            return jsonify({'directories': [], 'path': path})

        for entry in entries:
            if not entry.is_dir(follow_symlinks=False):
                continue
            try:
                total_size, file_count = get_dir_size(entry.path)
                children.append({
                    'path': entry.path,
                    'name': entry.name,
                    'size': total_size,
                    'size_formatted': disk_analyzer.format_size(total_size),
                    'file_count': file_count
                })
            except (PermissionError, OSError):
                continue

        # Filter zero-size and sort by size descending
        children = [c for c in children if c['size'] > 0]
        children.sort(key=lambda x: x['size'], reverse=True)

        return jsonify({
            'path': path,
            'directories': children
        })
    except Exception as e:
        print(f"Treemap data error: {e}")
        return jsonify({'error': str(e)}), 500

