@app.route('/api/disk/scan', methods=['POST'])
def trigger_disk_scan():
    """Trigger asynchronous filesystem scan (non-blocking)"""
    global disk_scan_in_progress
    
    if disk_scan_in_progress or disk_analyzer.scan_in_progress:
        return jsonify({'message': 'Scan already in progress', 'success': False}), 400
    
    try:
        data = request.json or {}
        root_path = data.get('path', None)
        
        # If no path specified, scan from drive root for full coverage
        if root_path is None:
            import os
            import platform
            
            if platform.system() == 'Windows':
                # Get the drive letter of current directory (usually C:\)
                current_drive = os.path.splitdrive(os.getcwd())[0]
                root_path = current_drive + '\\'  # e.g., 'C:\\'
            else:
                # On Unix/Linux/Mac, scan from root
                root_path = '/'
        
        disk_scan_in_progress = True
        
        print(f"Starting full drive scan from: {root_path}")
        print("This may take a few minutes on first scan...")
        
        # Callback when scan completes
        def scan_complete_callback(result):
            global disk_scan_in_progress
            disk_scan_in_progress = False
            print(f"Full drive scan completed: {len(result)} directories tracked")
        
        # Use async scan for non-blocking operation
        success = disk_analyzer.scan_filesystem_async(root_path, scan_complete_callback)
        
        if not success:
            disk_scan_in_progress = False
            return jsonify({'message': 'Failed to start scan', 'success': False}), 500
        
        return jsonify({
            'message': 'Full drive scan started (async)', 
            'success': True,
            'scan_path': root_path
        })
    except Exception as e:
        disk_scan_in_progress = False
        print(f"Disk scan error: {e}")
        return jsonify({'message': 'Failed to start scan', 'success': False}), 500

