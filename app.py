@app.route('/api/chat', methods=['POST'])
def chat():
    """Handle chatbot requests with Groq API"""
    try:
        data = request.json
        user_message = data.get('message', '')
        context = data.get('context', '')
        
        # Call Groq API
        headers = {
            'Authorization': f'Bearer {GROQ_API_KEY}',
            'Content-Type': 'application/json'
        }
        
        payload = {
            'model': 'mixtral-8x7b-32768',
            'messages': [
                {
                    'role': 'system',
                    'content': f'You are a helpful system monitoring assistant. {context}. Provide concise, helpful responses about system performance.'
                },
                {
                    'role': 'user',
                    'content': user_message
                }
            ],
            'temperature': 0.7,
            'max_tokens': 200
        }
        
        response = requests.post(GROQ_API_URL, headers=headers, json=payload, timeout=10)
        
        if response.status_code == 200:
            result = response.json()
            ai_response = result['choices'][0]['message']['content']
            return jsonify({'response': ai_response})
        else:
            return jsonify({'response': 'I apologize, but I am having trouble connecting to my AI service. Please try again.'}), 200
            
    except Exception as e:
        print(f"Groq API error: {e}")
        return jsonify({'response': 'I encountered an error. Please try asking again.'}), 200

def monitoring_loop():
    """Background monitoring loop"""
    global monitoring_active, anomaly_history, last_anomaly_time
    
    while monitoring_active:
        metrics = monitor.collect_metrics()
        detector.add_sample(metrics)
        anomaly = detector.detect(metrics)
        
        data = {
            'metrics': metrics,
            'anomaly': None,  # Don't send anomaly every time
            'is_trained': detector.is_trained,
            'baseline': detector.baseline_stats if detector.is_trained else None,
            'sample_count': detector.sample_count,
            'learning_period': detector.learning_period
        }
        
        # Anomaly detection disabled - only count them silently
        if anomaly:
            current_time = time.time()
            if current_time - last_anomaly_time > 30:  # 30 second cooldown
                anomaly_history.append(anomaly)
                # Don't send anomaly to frontend - just count it
                # data['anomaly'] = anomaly
                data['anomaly_history'] = anomaly_history[-10:]
                last_anomaly_time = current_time
        
        socketio.emit('update', data)
        time.sleep(1)

@socketio.on('connect')
def handle_connect():
    print('Client connected')
    emit('status', {'monitoring': monitoring_active})

@socketio.on('start_monitoring')
def handle_start():
    global monitoring_active
    if not monitoring_active:
        monitoring_active = True
        thread = threading.Thread(target=monitoring_loop, daemon=True)
        thread.start()
        emit('status', {'monitoring': True}, broadcast=True)

@socketio.on('stop_monitoring')
def handle_stop():
    global monitoring_active
    monitoring_active = False
    emit('status', {'monitoring': False}, broadcast=True)

